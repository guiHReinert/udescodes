package main;

public class Dono{
    private String nome;
    private String endereco;
    private String cpf;

    public String getNome(){
        return nome;
    }
    public void setNome(String n){
        nome = n;
    }

    public String getEndereco(){
        return endereco;
    }
    public void setEndereco(String e){
        endereco = e;
    }

    public String getCPF(){
        return cpf;
    }
    public void setCPF(String c){
        nome = c;
    }
}